"""
doc_generator.py — ReportLab PDF generator for Vaakya contracts.

Public API:
    generate_contract_pdf(state, document_title="") -> bytes

Two modes:
  • state["draft"] is set  → formal contract PDF  (new_doc sub-graph)
  • state["draft"] is empty → redline report PDF   (redline sub-graph)
"""

